"""Build phase-2 raw seed candidates for data expansion.

This script starts the second-stage data expansion. It intentionally writes to
data/raw only, keeps every row as needs_revision, and does not merge anything
into the processed training set.
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any


ROOT = Path(__file__).resolve().parents[1]
OUTPUT_PATH = ROOT / "data/raw/phase2_seed_candidates.jsonl"
BATCH = "phase2_v1_context_hard_negative"


def context(
    *,
    title: str,
    topic: str,
    parent: str = "",
    replies: list[str] | None = None,
    description: str = "",
    time: str = "",
) -> dict[str, Any]:
    return {
        "title": title,
        "description": description,
        "parent_comment": parent,
        "reply_chain": replies or [],
        "time": time,
        "topic": topic,
    }


def evidence_strength(risk: str) -> str:
    return {"high": "strong", "medium": "moderate", "low": "weak", "none": "weak"}[risk]


def ambiguity(risk: str) -> str:
    return {"high": "medium", "medium": "medium", "low": "high", "none": "low"}[risk]


def reasoning(case: dict[str, Any]) -> dict[str, Any]:
    risk = case["risk"]
    text = case["text"]
    if risk == "none":
        return {
            "literal_analysis": [f"字面上是在表达：{text}"],
            "encoding_analysis": ["未发现稳定的非字面风险编码；关键词或缩写只作为召回线索。"],
            "context_analysis": [case["context_note"]],
            "supporting_evidence": case.get("support", []),
            "counter_evidence": case["counter"],
            "final_rationale": "普通语境和反证更强，暂标为 none；该条用于训练模型避免关键词误杀。",
        }
    if risk == "low":
        return {
            "literal_analysis": [f"字面上是在表达：{text}"],
            "encoding_analysis": [case["encoding_note"]],
            "context_analysis": [case["context_note"]],
            "supporting_evidence": case["support"],
            "counter_evidence": case["counter"],
            "final_rationale": "存在弱风险信号，但普通解释仍较强，暂标为 low。",
        }
    return {
        "literal_analysis": [f"字面上是在表达：{text}"],
        "encoding_analysis": [case["encoding_note"]],
        "context_analysis": [case["context_note"]],
        "supporting_evidence": case["support"],
        "counter_evidence": case["counter"],
        "final_rationale": f"文本和上下文共同支持风险解释，暂标为 {risk}；仍需人工复核。",
    }


def build_sample(index: int, case: dict[str, Any]) -> dict[str, Any]:
    risk = case["risk"]
    sample_id = f"PHASE2_SEED_{index:04d}_{case['cluster_code']}_{risk.upper()}"
    return {
        "id": sample_id,
        "source_type": "synthetic",
        "platform": case["platform"],
        "text": case["text"],
        "context": case["context"],
        "risk_level": risk,
        "encoding_primary": "none" if risk == "none" else case["primary"],
        "encoding_secondary": case.get("secondary", []),
        "context_required": case.get("context_required", True),
        "ambiguity": case.get("ambiguity", ambiguity(risk)),
        "evidence_strength": case.get("evidence_strength", evidence_strength(risk)),
        "hard_negative": case.get("hard_negative", risk in {"none", "low"}),
        "target_known": case.get("target_known", False),
        "target_reference": case.get("target_reference", "匿名化风险语境"),
        "should_explain_target": False,
        "freshness": "stable",
        "reasoning": reasoning(case),
        "quality_status": "needs_revision",
        "review_notes": (
            f"source=phase2_seed; batch={BATCH}; axis={case['axis']}; "
            f"cluster={case['cluster']}; variant={case['variant']}; not_merged; needs_human_review"
        ),
    }


